// Arithmethic Operator

let a = 10;
let b = 3;

// Penjumlahan
console.log(a+b); // 13

// Pengurangan
console.log(a-b) // 7

// Perkalian
console.log(a*b) // 30

// Pembagian
console.log(a/b) // 3.333...

// Modulus (Sisa Bagi)
console.log(a%b) // 1

// Eksponen (Pangkat)
console.log(a**b) // 1000

// Increment
a++;
console.log(a) //11

// Decrement
b--;
console.log(b) // 2

// Comparison Operator

let passwordDaftar = "rahasia";
let passwordIsi = "rahasia";

// apakah passwordDaftar sama dengan passwordIsi?
// output: true
console.log(passwordDaftar == passwordIsi)

// apakah passwordDaftar tidak sama dengan passwordIsi?
// output: false
console.log(passwordDaftar != passwordIsi)

